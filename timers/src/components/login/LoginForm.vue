<template>
  <form @submit.prevent="handleSubmit" class="auth-form">
    <!-- 手机号输入 -->
    <div class="form-group">
      <label class="form-label">手机号</label>
      <div class="input-wrapper">
        <font-awesome-icon icon="phone" class="input-icon" />
        <input
          v-model="formData.phoneNumber"
          type="tel"
          class="form-input"
          :class="{ 'error': errors.phoneNumber }"
          placeholder="请输入手机号"
          maxlength="11"
          required
          @input="validatePhoneNumber"
          @blur="validatePhoneNumber"
        />
      </div>
      <div v-if="errors.phoneNumber" class="error-message">
        {{ errors.phoneNumber }}
      </div>
    </div>

    <!-- 密码输入 -->
    <div class="form-group">
      <label class="form-label">密码</label>
      <div class="input-wrapper">
        <font-awesome-icon icon="lock" class="input-icon" />
        <input
          v-model="formData.password"
          :type="showPassword ? 'text' : 'password'"
          class="form-input"
          :class="{ 'error': errors.password }"
          placeholder="请输入密码"
          minlength="6"
          required
          @blur="validatePassword"
        />
        <button
          type="button"
          class="password-toggle"
          @click="showPassword = !showPassword"
        >
          <font-awesome-icon :icon="showPassword ? 'eye-slash' : 'eye'" />
        </button>
      </div>
      <div v-if="errors.password" class="error-message">
        {{ errors.password }}
      </div>
    </div>

    <!-- 提交按钮 -->
    <button
      type="submit"
      class="auth-btn"
      :disabled="isLoading || !isFormValid"
      :class="{ 'loading': isLoading }"
    >
      <font-awesome-icon v-if="isLoading" icon="spinner" spin class="btn-icon" />
      <font-awesome-icon v-else icon="sign-in-alt" class="btn-icon" />
      {{ isLoading ? '登录中...' : '登录' }}
    </button>

    <!-- 错误/成功消息 -->
    <div v-if="message.text" :class="['message', message.type]">
      <font-awesome-icon 
        :icon="message.type === 'error' ? 'exclamation-circle' : 'check-circle'" 
        class="message-icon" 
      />
      {{ message.text }}
    </div>

    <!-- 切换到注册 -->
    <div class="switch-mode">
      还没有账号？
      <button type="button" class="switch-link" @click="$emit('switch-mode', 'register')">
        立即注册
      </button>
    </div>
  </form>
</template>

<script>
import { ref, computed, reactive } from 'vue'
import { FontAwesomeIcon } from '@fortawesome/vue-fontawesome'

export default {
  name: 'LoginForm',
  components: {
    FontAwesomeIcon
  },
  emits: ['switch-mode', 'submit'],
  setup(props, { emit }) {
    // 表单数据
    const formData = reactive({
      phoneNumber: '',
      password: ''
    })

    // 表单状态
    const isLoading = ref(false)
    const showPassword = ref(false)
    const errors = reactive({
      phoneNumber: '',
      password: ''
    })
    const message = reactive({
      text: '',
      type: ''
    })

    // 验证手机号
    const validatePhoneNumber = () => {
      const phoneRegex = /^1[3-9]\d{9}$/
      if (!formData.phoneNumber) {
        errors.phoneNumber = '请输入手机号'
      } else if (!phoneRegex.test(formData.phoneNumber)) {
        errors.phoneNumber = '请输入正确的11位手机号'
      } else {
        errors.phoneNumber = ''
      }
    }

    // 验证密码
    const validatePassword = () => {
      if (!formData.password) {
        errors.password = '请输入密码'
      } else if (formData.password.length < 6) {
        errors.password = '密码长度至少6位'
      } else {
        errors.password = ''
      }
    }

    // 表单是否有效
    const isFormValid = computed(() => {
      return formData.phoneNumber && 
             formData.password && 
             !errors.phoneNumber && 
             !errors.password
    })

    // 显示消息
    const showMessage = (text, type = 'error') => {
      console.log(`${type === 'error' ? '❌' : '✅'} ${text}`)
      message.text = text
      message.type = type
      setTimeout(() => {
        message.text = ''
        message.type = ''
      }, 3000)
    }

    // 表单提交
    const handleSubmit = async () => {
      console.log('🔄 登录表单提交')
      
      // 验证表单
      validatePhoneNumber()
      validatePassword()

      if (!isFormValid.value) {
        showMessage('请正确填写所有字段')
        return
      }

      isLoading.value = true

      try {
        console.log('📤 提交登录数据:', { 
          phoneNumber: formData.phoneNumber,
          password: '***'
        })
        
        await emit('submit', { ...formData })
        showMessage('登录成功！', 'success')
        
      } catch (error) {
        console.error('❌ 登录失败:', error)
        const errorMessage = error.response?.data?.message || error.message || '登录失败，请重试'
        showMessage(errorMessage)
      } finally {
        isLoading.value = false
      }
    }

    return {
      formData,
      errors,
      message,
      isLoading,
      showPassword,
      isFormValid,
      validatePhoneNumber,
      validatePassword,
      handleSubmit,
      showMessage
    }
  }
}
</script>

